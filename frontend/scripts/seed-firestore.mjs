import { readFile } from 'node:fs/promises';
import { resolve } from 'node:path';
import { initializeApp } from 'firebase/app';
import {
  addDoc,
  collection,
  doc,
  getFirestore,
  setDoc,
} from 'firebase/firestore/lite';

function parseEnvFile(contents) {
  return contents
    .split(/\r?\n/)
    .map((line) => line.trim())
    .filter((line) => line && !line.startsWith('#'))
    .reduce((env, line) => {
      const separatorIndex = line.indexOf('=');
      if (separatorIndex === -1) {
        return env;
      }

      const key = line.slice(0, separatorIndex).trim();
      const rawValue = line.slice(separatorIndex + 1).trim();
      env[key] = rawValue.replace(/^['"]|['"]$/g, '');
      return env;
    }, {});
}

async function loadFirebaseConfig() {
  const envPath = resolve(process.cwd(), '.env');
  const envFile = await readFile(envPath, 'utf8');
  const env = parseEnvFile(envFile);

  const firebaseConfig = {
    apiKey: env.VITE_FIREBASE_API_KEY,
    authDomain: env.VITE_FIREBASE_AUTH_DOMAIN,
    projectId: env.VITE_FIREBASE_PROJECT_ID,
    storageBucket: env.VITE_FIREBASE_STORAGE_BUCKET,
    messagingSenderId: env.VITE_FIREBASE_MESSAGING_SENDER_ID,
    appId: env.VITE_FIREBASE_APP_ID,
    measurementId: env.VITE_FIREBASE_MEASUREMENT_ID,
  };

  const missing = Object.entries(firebaseConfig)
    .filter(([, value]) => !value)
    .map(([key]) => key);

  if (missing.length > 0) {
    throw new Error(`Missing Firebase config values in .env: ${missing.join(', ')}`);
  }

  return firebaseConfig;
}

async function seed() {
  const firebaseConfig = await loadFirebaseConfig();
  const app = initializeApp(firebaseConfig);
  const db = getFirestore(app);
  const nowIso = new Date().toISOString();

  await setDoc(
    doc(db, 'devlog', 'current'),
    {
      content: `# DevLog AI

## Overview

Dashboard Firebase seed is live. This document will be replaced by backend-generated project memory once the watcher and Gemini pipeline are connected.

## Frontend

- React dashboard scaffold is connected to Firestore.
- Real-time listeners are ready for changes, decisions, snapshots, and todos.

## Backend

- Waiting on live diff analysis and DevLog updates from the FastAPI backend.

## Todos

- Verify the backend is writing to all agreed Firestore collections.
- Replace this seed content with live project state.`,
      lastUpdated: nowIso,
      createdBy: 'frontend-seed-script',
      seededAt: nowIso,
    },
    { merge: true },
  );

  await setDoc(
    doc(db, 'status', 'current'),
    {
      projectHealth: 'yellow',
      lastUpdated: nowIso,
      files: {},
      createdBy: 'frontend-seed-script',
      seededAt: nowIso,
    },
    { merge: true },
  );

  await addDoc(collection(db, 'changes'), {
    file: 'frontend/src/App.jsx',
    timestamp: nowIso,
    classification: 'config',
    summary: 'Seeded the dashboard Firestore collections so the live UI has initial data.',
    danger: false,
    agent: 'frontend-seed-script',
    seeded: true,
    createdAt: nowIso,
  });

  await addDoc(collection(db, 'decisions'), {
    timestamp: nowIso,
    source: 'User',
    type: 'decision',
    summary: 'Initialized Firestore collections for the DevLog AI dashboard.',
    details: 'These seed entries can be replaced by live MCP and backend writes during integration.',
    seeded: true,
    createdAt: nowIso,
  });

  await addDoc(collection(db, 'snapshots'), {
    timestamp: nowIso,
    reason: 'initial',
    title: 'Initial Firestore seed',
    summary: 'Starter snapshot created so the snapshots drawer has a valid document shape.',
    content: 'This is a placeholder snapshot generated by the frontend seed script.',
    seeded: true,
    createdAt: nowIso,
  });

  await addDoc(collection(db, 'todos'), {
    title: 'Replace seed data with live backend writes',
    state: 'in-progress',
    updatedAt: nowIso,
    seeded: true,
    createdAt: nowIso,
  });

  console.log('Firestore seed completed successfully.');
  process.exit(0);
}

seed().catch((error) => {
  console.error('Firestore seed failed.');
  console.error(error);
  process.exitCode = 1;
});
